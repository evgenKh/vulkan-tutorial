void VulkanDriver::initVulkan(uint32_t width, uint32_t height, GLFWwindow* glfwWindow)
    {
        this->width = width;
        this->height = height;
        this->glfwWindow = glfwWindow;

        createInstance();
        setupDebugMessenger();
        createSurface(glfwWindow);
        pickPhysicalDevice();
        createLogicalDevice();

        int frameBufferWidth, frameBufferHeight;
        glfwGetFramebufferSize(glfwWindow, &frameBufferWidth, &frameBufferHeight);

        swapChainUtils.createSwapChain(physicalDevice, device, surface, frameBufferWidth, frameBufferHeight);
        swapChainUtils.createImageViews(device);
        createRenderPass();
        createDescriptorSetLayout();//uniforms
        createGraphicsPipeline();
        swapChainUtils.createFramebuffers(device, renderPass);
        createCommandPool();
        
        imageUtils.createTextureImage(device);
        imageUtils.createTextureImageView();
        imageUtils.createTextureSampler();

        createVertexBuffer();
        createIndexBuffer();
        createUniformBuffers();
        createDescriptorPool();
        createDescriptorSets();
        createCommandBuffers();
        createSyncObjects();
    }

    void VulkanDriver::createInstance()
    {
        VkApplicationInfo appInfo{};
        appInfo.sType = VK_STRUCTURE_TYPE_APPLICATION_INFO;
        appInfo.pApplicationName = "Hello Triangle";
        appInfo.applicationVersion = VK_MAKE_VERSION(1, 0, 0);
        appInfo.pEngineName = "No Engine";
        appInfo.engineVersion = VK_MAKE_VERSION(1, 0, 0);
        appInfo.apiVersion = VK_API_VERSION_1_0;

        VkInstanceCreateInfo createInfo{};
        createInfo.sType = VK_STRUCTURE_TYPE_INSTANCE_CREATE_INFO;
        createInfo.pApplicationInfo = &appInfo;
        createInfo.enabledLayerCount = 0;

        auto extensions = getRequiredExtensionsInstance();
        createInfo.enabledExtensionCount = extensions.size();
        createInfo.ppEnabledExtensionNames = extensions.data();

        //Validation layers
        if (enableValidationLayers && !checkValidationLayerSupport())
        {
            throw std::runtime_error("validation layers requested, but not available!");
        }
        if (enableValidationLayers)
        {
            createInfo.enabledLayerCount = static_cast<uint32_t>(validationLayers.size());
            createInfo.ppEnabledLayerNames = validationLayers.data();
        }
        else
        {
            createInfo.enabledLayerCount = 0;
        }

        if (vkCreateInstance(&createInfo, nullptr, &instance) != VK_SUCCESS)
        {
            throw std::runtime_error("failed to create instance!");
        }
    }

